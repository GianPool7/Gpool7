const modoClaro=document.getElementById("claro");
const modoOscuro=document.getElementById("oscuro");
// los iconos
const modoDia=document.getElementById("dia")
const modoNoche=document.getElementById("noche")
// ocultar
modoClaro.style.display="none"
//modoOscuro.style.display="none";

/* */
const fnd=document.getElementById("fondo")
/* */


const vinculos=document.querySelectorAll("a");
const nombre=document.querySelectorAll("h1,h2,h3");
const contenedor=document.getElementById("caja");
const text=document.querySelectorAll("p");

/* */
modoClaro.addEventListener("click",()=>{
    modoClaro.style.display="none";
    modoOscuro.style.display="flex"
    // poner el fondo oscuro del header
    fnd.style.backgroundColor="";
    vinculos.forEach(enlace => {
        enlace.style.color = "black";
    });
    nombre.forEach(titulo=>{
        titulo.style.color="black";
    });    
    text.forEach(texto=>{
        texto.style.color="black"
    })
    contenedor.style.backgroundColor="white"
})

modoOscuro.addEventListener("click",()=>{
    modoOscuro.style.display="none"
    modoClaro.style.display="flex"
    // poner el fondo oscuro del header
    fnd.style.backgroundColor="#6d8582";
    fnd.style.boxShadow="1px 2px 10px white"
    vinculos.forEach(enlace => {
        enlace.style.color = "white";
    });
    nombre.forEach(titulo=>{
        titulo.style.color="white";
    });
    text.forEach(texto=>{
        texto.style.color="white"
    })
    contenedor.style.backgroundColor="#1A202C"
})

